package dto;

/**
 *
 * @author admin
 */
public class User {
    
    private String email;
    private String pass;
    private String userName;
    private String phone;
    private String address;
    private String date;
    private String role;
    private String status;

    public User(String email, String pass, String userName, String phone, String address, String date, String role, String status) {
        this.email = email;
        this.pass = pass;
        this.userName = userName;
        this.phone = phone;
        this.address = address;
        this.date = date;
        this.role = role;
        this.status = status;
    }

    public User(String email, String pass, String userName,  String role, String status,String date) {
        this.email = email;
        this.pass = pass;
        this.userName = userName;
        this.date = date;
        this.role = role;
        this.status = status;
    }
    
    

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the pass
     */
    public String getPass() {
        return pass;
    }

    /**
     * @param pass the pass to set
     */
    public void setPass(String pass) {
        this.pass = pass;
    }

    /**
     * @return the userName
     */
    public String getUserName() {
        return userName;
    }

    /**
     * @param userName the userName to set
     */
    public void setUserName(String userName) {
        this.userName = userName;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the role
     */
    public String getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(String role) {
        this.role = role;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }
    
    
    
}
